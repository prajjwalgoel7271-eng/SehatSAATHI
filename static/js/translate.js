// ── Core translate function ─────────────────────────────────────────────────
function translatePage(lang, animate) {
    const dict = translationMap[lang] || translationMap['en'];

    // Update <html lang> for proper Indic font rendering
    document.documentElement.lang = langTagMap[lang] || 'en';

    // Collect all translatable elements
    const els = document.querySelectorAll('[data-trn]');

    if (animate) {
        // Smooth fade-out → swap → fade-in
        els.forEach(el => { el.style.opacity = '0'; });
        setTimeout(() => {
            _applyTranslations(els, dict);
            els.forEach(el => { el.style.opacity = '1'; });
        }, 180);
    } else {
        _applyTranslations(els, dict);
    }

    // Sync every language selector on the page
    document.querySelectorAll('.ss-lang-select').forEach(sel => {
        if (sel.value !== lang) sel.value = lang;
    });
    const reportSelect = document.getElementById('lang-select');
    if (reportSelect && reportSelect.value !== lang) {
        reportSelect.value = lang;
        reportSelect.dispatchEvent(new Event('change', { bubbles: true }));
    }
}

function _applyTranslations(els, dict) {
    els.forEach(el => {
        const key = el.getAttribute('data-trn');
        if (!dict[key]) return;

        // For <option> elements, set textContent (innerHTML breaks select options)
        if (el.tagName === 'OPTION') {
            el.textContent = dict[key];
        } else {
            el.innerHTML = dict[key];
        }

        // Translate placeholder attribute if the key + '-ph' exists
        if (el.hasAttribute('placeholder') && dict[key + '-ph']) {
            el.setAttribute('placeholder', dict[key + '-ph']);
        }
        // Translate title attribute if the key + '-tip' exists
        if (el.hasAttribute('title') && dict[key + '-tip']) {
            el.setAttribute('title', dict[key + '-tip']);
        }
    });
}

// ── Switch language (called from any selector) ─────────────────────────────
function switchLanguage(lang) {
    localStorage.setItem('sehatsaathi_lang', lang);
    translatePage(lang, true);
}

