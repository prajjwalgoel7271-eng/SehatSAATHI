// ── Build the navbar <select> ───────────────────────────────────────────────
function _buildNavbarSelect(activeLang) {
    const navLinks = document.querySelector('.nav-links');
    if (!navLinks || document.getElementById('global-lang-select')) return;

    const sel = document.createElement('select');
    sel.id = 'global-lang-select';
    sel.className = 'nav-btn ss-lang-select';
    Object.assign(sel.style, {
        background: 'rgba(255,255,255,0.08)',
        border: '1px solid var(--border-color)',
        color: 'white',
        padding: '0.5rem 0.6rem',
        borderRadius: '8px',
        fontFamily: "'Outfit', sans-serif",
        fontSize: '0.9rem',
        cursor: 'pointer',
        outline: 'none',
        marginLeft: '0.5rem'
    });

    langOptions.forEach(opt => {
        const o = document.createElement('option');
        o.value = opt.val;
        o.textContent = opt.label;
        o.style.backgroundColor = '#16161e';
        o.style.color = 'white';
        if (opt.val === activeLang) o.selected = true;
        sel.appendChild(o);
    });

    navLinks.insertBefore(sel, navLinks.firstChild);
    sel.addEventListener('change', e => switchLanguage(e.target.value));
}

// ── Build the mobile floating action button + panel ─────────────────────────
function _buildMobileFAB(activeLang) {
    if (document.getElementById('ss-lang-fab')) return;

    // FAB button
    const fab = document.createElement('button');
    fab.id = 'ss-lang-fab';
    fab.className = 'ss-lang-fab';
    fab.setAttribute('aria-label', 'Change language');
    fab.textContent = '🌐';

    // Panel
    const panel = document.createElement('div');
    panel.id = 'ss-lang-panel';
    panel.className = 'ss-lang-panel';

    langOptions.forEach(opt => {
        const btn = document.createElement('button');
        btn.className = 'ss-lang-opt' + (opt.val === activeLang ? ' active' : '');
        btn.setAttribute('data-lang', opt.val);
        btn.innerHTML = `<span style="font-size:1.2rem">${opt.flag}</span> ${opt.label.split(' ').slice(1).join(' ')}`;
        btn.addEventListener('click', () => {
            switchLanguage(opt.val);
            // Update active states
            panel.querySelectorAll('.ss-lang-opt').forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            panel.classList.remove('open');
        });
        panel.appendChild(btn);
    });

    fab.addEventListener('click', (e) => {
        e.stopPropagation();
        panel.classList.toggle('open');
    });

    // Close panel on outside click
    document.addEventListener('click', () => panel.classList.remove('open'));
    panel.addEventListener('click', e => e.stopPropagation());

    document.body.appendChild(panel);
    document.body.appendChild(fab);
}

// ── MutationObserver: auto-translate dynamically added elements ─────────────
function _startObserver() {
    const lang = localStorage.getItem('sehatsaathi_lang') || 'en';
    const dict = translationMap[lang] || translationMap['en'];

    const observer = new MutationObserver(mutations => {
        const currentLang = localStorage.getItem('sehatsaathi_lang') || 'en';
        const currentDict = translationMap[currentLang] || translationMap['en'];
        let found = false;

        mutations.forEach(mutation => {
            mutation.addedNodes.forEach(node => {
                if (node.nodeType !== 1) return;
                // Translate the node itself if it has data-trn
                if (node.hasAttribute && node.hasAttribute('data-trn')) {
                    const key = node.getAttribute('data-trn');
                    if (currentDict[key]) {
                        node.tagName === 'OPTION'
                            ? (node.textContent = currentDict[key])
                            : (node.innerHTML = currentDict[key]);
                    }
                    found = true;
                }
                // Translate children with data-trn
                if (node.querySelectorAll) {
                    const children = node.querySelectorAll('[data-trn]');
                    if (children.length) {
                        _applyTranslations(children, currentDict);
                        found = true;
                    }
                }
            });
        });
    });

    observer.observe(document.body, { childList: true, subtree: true });
}

// ── Init on DOMContentLoaded ────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
    const activeLang = localStorage.getItem('sehatsaathi_lang') || 'en';

    _injectTranslateCSS();
    _buildNavbarSelect(activeLang);
    _buildMobileFAB(activeLang);

    // Initial translation (no animation on page load)
    translatePage(activeLang, false);

    // Watch for dynamically added content
    _startObserver();
});
